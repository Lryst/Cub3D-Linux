#include "libft.h"

char	*ft_strndup(const char *s, int n)
{
	char	*res;
	int		i;

	i = 0;
	res = (char*)malloc(sizeof(char) * (n + 1));
	if (res == NULL)
		return (NULL);
	while (i < n)
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
